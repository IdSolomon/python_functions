# In order for me to make my functions a bit more dynamic, 
# I am going to use the concept of function parameters.

# Function parameters allow my functions to accept data as an input value
# between the parenthesis of a function.

def generateSchoolForMutants(schoolName):
    print("The " + schoolName + " can be found in a very rural area of New York state.")

generateSchoolForMutants("Xavier's School for Gifted Youngsters")

# OUTPUT: The Xavier's School for Gifted Youngsters can be found in a very rural area of New York state.

# My parameter of schoolName is used by passing in an argument to the function when I call it!